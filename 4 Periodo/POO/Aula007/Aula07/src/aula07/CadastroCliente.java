    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        
        String sql = "DELETE FROM cliente WHERE codigo="+codigo;
        Conexao conexao = new Conexao();
        try {
            conexao.statement.execute(sql);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de sintaxe da senten√ßa SQL");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nome = txtNome.getText();
        String CPF = txtCPF.getText();
        
        String sql = "INSERT INTO cliente VALUES (" + codigo + ",'" + nome + "','" + CPF +"')";
        Conexao conexao = new Conexao();
        try {
            conexao.statement.execute(sql);
            JOptionPane.showMessageDialog(null, "Cliente salvo com sucesso");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de sintaxe da senten√ßa SQL");
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        
        String sql = "SELECT * FROM cliente WHERE codigo="+codigo;
        Conexao conexao = new Conexao();
        try {
            ResultSet rs = conexao.statement.executeQuery(sql);
            while(rs.next()){
                txtNome.setText(rs.getString("nome"));
                txtCPF.setText(rs.getString("cpf"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de sintaxe da senten√ßa SQL");
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nome = txtNome.getText();
        String CPF = txtCPF.getText();
        
        String sql = "UPDATE cliente SET nome='"+nome+"',cpf='"+CPF+"' WHERE codigo = "+codigo;
        Conexao conexao = new Conexao();
        try {
            conexao.statement.execute(sql);
            JOptionPane.showMessageDialog(null, "Atualizado com sucesso");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de sintaxe da senten√ßa SQL");
        }
        
    }//GEN-LAST:event_btnEditarActionPerformed

