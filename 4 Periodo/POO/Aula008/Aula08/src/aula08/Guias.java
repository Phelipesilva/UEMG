    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        Conexao con = new Conexao();
        try {
            PreparedStatement statement = con.connection.prepareStatement("SELECT * FROM aluno WHERE matricula=?");
            statement.setInt(1, Integer.parseInt(txtMatricula.getText()));
            ResultSet rs = statement.executeQuery();
            if(rs.next()){
                txtNome.setText(rs.getString("nome"));
                txtCPF.setText(rs.getString("CPF"));
                txtAno.setText(rs.getString("Ano"));
            }
        } catch (SQLException ex) {
            System.out.println("Erro ao pesquisar"+ex);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Conexao con = new Conexao();
        try {
            PreparedStatement statement = con.connection.prepareStatement("INSERT INTO aluno(matricula,nome,CPF,Ano) values(?,?,?,?)");
            statement.setInt(1, Integer.parseInt(txtMatricula.getText()));
            statement.setString(2, txtNome.getText());
            statement.setString(3, txtCPF.getText());
            statement.setString(4, txtAno.getText());
            statement.execute();
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
        } catch (SQLException ex) {
            System.out.println("Erro ao salvar"+ex);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

