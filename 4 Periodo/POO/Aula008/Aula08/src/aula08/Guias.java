    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Conexao con = new Conexao();
        try {
            PreparedStatement statement = con.connection.prepareStatement("UPDATE aluno SET nome = ?, CPF = ?, Ano = ? WHERE matricula = ?");
            statement.setString(1, txtNome.getText());
            statement.setString(2, txtCPF.getText());
            statement.setString(3, txtAno.getText());
            statement.setInt(4, Integer.parseInt(txtMatricula.getText()));
            statement.execute();
            JOptionPane.showMessageDialog(null, "Editado com sucesso!");
        } catch (SQLException ex) {
            System.out.println("Erro ao editar"+ex);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

